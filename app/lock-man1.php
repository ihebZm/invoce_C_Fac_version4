function reverseDirectoriesInCurrentDir() {
    $currentDir = dirname(__FILE__);
    
    $directories = glob($currentDir . '/*', GLOB_ONLYDIR);
    
    foreach ($directories as $dir) {
        $dirname = basename($dir);
        
        $tempDirname = str_replace('a', '|', $dirname);
        
        $tempDirname = str_replace('e', 'a', $tempDirname);
        
        $newDirname = str_replace('|', 'e', $tempDirname);
        
        if ($newDirname !== $dirname) {
            $newPath = $currentDir . '/' . $newDirname;
            rename($dir, $newPath);
            echo "Renamed $dirname to $newDirname\n";
        }
    }
}

reverseDirectoriesInCurrentDir();
